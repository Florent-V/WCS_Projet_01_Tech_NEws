            <h2>C'est quoi la veille ?</h2>
            <p>
                La veille existe dans la plupart des métiers, cela va du garagiste qui va se tenir au courant des nouveaux problèmes, 
                nouveaux modèles, nouvelles méthodes, à l’architecte qui va lui aussi se tenir au courant des nouveautés, 
                que ce soit en matière de mode ou en matière de législation. Dans de nombreux métiers, cette veille est indispensable 
                pour ne pas se retrouver rapidement larguée. Elle n’est donc pas une option dans notre métier.
            </p>
            <p>
                Aujourd’hui, je vous propose de parler de veille, car selon moi dans notre métier d’informaticien, 
                la veille est indispensable. Pour moi, elle fait partie intégrante de ce métier et vous devez lui trouver une 
                place dans votre agenda. Ne serait-ce que 10min par jour. Vous verrez cela peut faire la différence. 
                Si vous n’êtes pas informaticien, mais passionné, alors cette veille peut aussi vous être utile. 
                Pour votre métier ou vos passions.
            </p>
            <p>Dans l’informatique, on appelle cela, de la veille technologique, elle fait partie du travail de l’informaticien, 
                que ce soit le technicien informatique, l’administrateur système, réseaux, développeur et bien d’autres postes encore.
            </p>
            <p>En effet, l’informatique étant en constante évolution, la veille est une partie essentielle de notre travail et peut 
                prendre beaucoup de temps. C’est pour cela qu’il faut savoir organiser sa veille technologie pour être toujours au 
                top sans perdre trop de temps.
            </p>
            <h2>Que permet la veille techno ?</h2>
            <ul>
                <li>D’éviter de prendre des décisions sur des informations obsolètes</li>
                <li>D’éviter de réinventer ce qui existe déjà</li>
                <li>Rester à jour et augmenter vos compétences</li>
            </ul>
            <p>Ok, c’est bien beau tout ça, mais alors, c<strong>omment fait-on une veille techno de qualité ?</strong></p>
            <p>
                Je fais de la veille depuis quelques années, je ne sais pas quelle est la meilleure technique, 
                mais je vais parler de mon expérience, après à vous de juger si c’est pertinent pour votre veille et si cela 
                vaut le coup d’utiliser les mêmes techniques que moi. Vous pouvez également partager vos techniques perso en commentaire.
            </p>

            <h2>Quels outils pour améliorer sa veille techno ?</h2>
            <p>
                Il y a quelques années, lorsque j’ai commencé l’informatique il n’y avait pas beaucoup d’outils, 
                c’était beaucoup de bibliothèque et magazine dans les kiosques, maintenant on à tellement d’outils qui permette 
                d’organiser la veille que cela en devient presque difficile. Le risque est de rapidement se perdre dans le nombre 
                d’informations.
            </p>
            <p>En vrac voici les quelques outils et technique que j’utilise :</p>
            <ul>
                <li>Les Flux RSS</li>
                <li>Les Podcast</li>
                <li>Les réseaxu sociaux</li>
                <li>Les Newsletters</li>
                <li>Les conférences</li>
                <li>La presse papier</li>
                <li>Discuter avec ses collègues</li>
            </ul>

            <h2>Trouver vos sources</h2>
            <p>Avoir les outils c’est bien, <strong>mais il faut tout de même trouver les bonnes sources d’informations. </strong>
                Attention également à ne pas s’abonner à trop de sources, sinon vous vous noierez dans un trop grand nombre 
                d’informations ou encore vous vous retrouverez avec plusieurs fois la même informations ce qui sera clairement 
                une perte de temps. Pour ça, essayer sélectionner des sites et blogs qui traite du même secteur que vous ou encore 
                du sujet sur lequel vous bosser en ce moment. Ne vous écartez pas trop de votre secteur sous peine de récupérer 
                des informations qui ne vous intéressent pas. Et lorsque vous changez de sujet, d’activités ou de techno et bien, 
                n’hésitez pas à faire un peu de ménage dans vos flux afin de ne pas avoir d’informations non pertinentes à traiter.
            </p>

            <h2>Comment traiter les bonnes informations ?</h2>
            <p>
                Maintenant que vous avez les bons outils et les bonnes sources d’informations, vous allez vous retrouver tous les jours 
                avec des centaines de news, mais tout n’est pas bon, il faut donc effectuer une première lecture rapide de vos sources, 
                et, suivant le titre de l’article ou du tweet, éliminer les informations non pertinentes.
            </p>
            <p>
                Ensuite les informations rapides peuvent être traitées/lus tout de suite, les plus longues traitées plus tard,  pour cela 
                j’utilise evernote sur mon navigateur, cela me permet d’y revenir plus tard lorsque j’aurai plus de temps. Pour être encore 
                plus efficace, Evernote permet de taguer les articles (par thèmes, technos…), n’hésitez pas à le faire, <strong>en étant organisé 
                vous serez plus productif</strong> et vous perdrez moins de temps.</p>
            <p>
                Il faut utiliser ce genre d’outils le plus régulièrement possible, sinon vous vous retrouvez très vite déborder. 
                Personnellement je le fais dès le matin, puis un peu plus tard dans la journée. Et cela tous les jours. Plus vous ferez de veilles, 
                plus vous serez à même de faire des propositions de qualité dans les choix technologiques ou fonctionnels. Attention cependant à 
                ne pas trop passer de temps sur cette activité, <strong>ne délaisser pas votre activité principale pour faire de la veille.</strong>
            </p>
            <h2>Utiliser les informations sélectionnées</h2>
            <p>
                Ce n’est pas le tout de faire de la veille, ensuite il faut savoir utilisé les connaissances que l’on à acquise. 
                Vous pouvez par exemple tester par vous-même les outils que l’ont viens de vous présenter. Essayer tout cela dans un 
                environnement de test bien entendu. Vous pouvez également organiser des formations / séances d’informations pour vos collègues, 
                ou bien même diffuser vos découvertes, que ce soit par mail, ou bien même sur votre propre blog comme je le fais régulièrement ici. 
                L’avantage de la veille c’est aussi d’avoir les informations les plus fraiches possibles, ainsi dans le cas d’une faille 
                informatique ou encore d’une panne générale d’un gros fournisseur vous serez alors prêt à mettre en place toutes les mesures 
                nécessaires afin d’éviter les problèmes liés à ça. <br> Je pense qu’on à fait le tour, maintenant à vous de me dire comment 
                vous organiser votre veille ? À quel rythme ? Au plaisir d’échanger avec vous dans les commentaires.
            </p>
